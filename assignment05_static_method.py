# Assignment: Create a class MathUtils with a static method add(a, b) that returns the sum. 
# No class or instance variables should be used.

class Mathutils:
    @staticmethod
    def add(a, b):
        return a + b
    

# Example usage
result = Mathutils.add(5, 10)
print(f"The sum is: {result}")  
    


